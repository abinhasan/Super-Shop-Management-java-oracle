    private void oderItem() {
        
        if (codeTextField.getText().isEmpty()) {
            
            Message.error1(this, "Please Insert a Product First");
            codeTextField.requestFocus();
            return;
        }
        
        if (quantityTextField.getText().isEmpty()) {
            Message.error1(this, "Please Enter Quantity.");
            quantityTextField.requestFocus();
            return;
        }
        
        if (Double.parseDouble(quantityTextField.getText()) == 0) {
            Message.error1(this, "Quantity Field Must Not Be 0.");
            quantityTextField.requestFocus();
            return;
        }
        
        
        String sql, iCode;
        iCode = codeTextField.getText();
        Double Quantity = Double.parseDouble(quantityTextField.getText());
        sql = "select stock from administrator.openitem where UPPER(ITEM_CODE) = '" + iCode.toUpperCase() + "'";
        Object[] ob = Utility2.getSingleRowQueryObjects(sql, rootPane);
        
        if (Double.parseDouble(ob[0].toString()) < Quantity) {
            Message.error1(this, "Sorry! Only " + Double.parseDouble(ob[0].toString()) + " are Available for Sale.");
            return;
        }
        
        sql = "update administrator.openitem set stock=" + (Double.parseDouble(ob[0].toString()) - Quantity) + " where UPPER(ITEM_CODE) = '" + iCode.toUpperCase() + "'";
        Utility2.insertUpdateQuery(sql, rootPane, 4);
        
        orderItem[proCount][0] = codeTextField.getText();
        orderItem[proCount][1] = desTextField.getText();
        orderItem[proCount][2] = valueTextField.getText();
        orderItem[proCount][3] = discountTextField.getText();
        orderItem[proCount][4] = quantityTextField.getText();
        orderItem[proCount][5] = priceTextField.getText();
        orderItem[proCount++][6] = couponTextField.getText();
        totalPrice += Double.parseDouble(priceTextField.getText());
        totalTextField.setText(totalPrice + "");
        codeTextField.setText("");
        quantityTextField.setText("");
        priceTextField.setText("");
        desTextField.setText("");
        valueTextField.setText("");
        discountTextField.setText("");
        codeTextField.requestFocus();
        
        loaditemTable();
        calculateFinalPrice();
        
    }
    
