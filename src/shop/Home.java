    private void jButton13ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton13ActionPerformed
        // TODO add your handling code here:

        if (JOptionPane.showConfirmDialog(this, "Are you sure you want to delete this product?") == 0) {
            
            String item_code = itemTable.getValueAt(itemTable.getSelectedRow(), 0).toString();
            double pro_quantity = Double.parseDouble(itemTable.getValueAt(itemTable.getSelectedRow(), 4).toString());
            double pro_price = Double.parseDouble(itemTable.getValueAt(itemTable.getSelectedRow(), 5).toString());
            String sql = "select STOCK from administrator.OPENITEM where UPPER(ITEM_CODE)='" + item_code.toUpperCase() + "'";
            Object[] ob = Utility2.getSingleRowQueryObjects(sql, rootPane);
            sql = "update administrator.OPENITEM set STOCK=" + (Double.parseDouble(ob[0].toString()) + pro_quantity) + " where UPPER(ITEM_CODE)='" + item_code.toUpperCase() + "'";
            Utility2.insertUpdateQuery(sql, rootPane, 4);
            for (int i = itemTable.getSelectedRow(); i < proCount; i++) {
                orderItem[i] = orderItem[i + 1];
            }
            proCount--;
            loaditemTable();
            totalTextField.setText((totalPrice -= pro_price) + "");
            calculateFinalPrice();
        }
    }//GEN-LAST:event_jButton13ActionPerformed
    
