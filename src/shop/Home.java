    private void calculateFinalPrice() {
        
        double vat, discount, totalprice = 0, less, finalprice, recieved;
        
        if (vatTextField.getText().isEmpty()) {
            vat = 0;
        } else {
            vat = Double.parseDouble(vatTextField.getText());
        }
        
        if (discountTextField.getText().isEmpty()) {
            discount = 0;
        } else {
            discount = Double.parseDouble(discountTextField.getText());
        }
        
        for (int i = 0; i < proCount; i++) {
            totalprice += Double.parseDouble(orderItem[i][5]);
        }


        //totalprice = Double.parseDouble(totalTextField.getText());

        
        vat = ((vat * totalprice) / 100);
        discount = ((discount * totalprice) / 100);
        totalprice += (vat - discount);
        totalTextField.setText(totalprice + "");
        
        
        if (lessTextField.getText().isEmpty()) {
            less = 0;
        } else {
            less = Double.parseDouble(lessTextField.getText());
        }
        finalprice = (totalprice - less);
        
        jLabel20.setText(finalprice + "");
        
        if (PayableTextField.getText().isEmpty()) {
            recieved = 0;
        } else {
            recieved = Double.parseDouble(PayableTextField.getText());
        }
        
        if (recieved >= finalprice) {
            advanceTextField.setText(finalprice + "");
            payableTextField.setText("0");
            changeTextField.setText((finalprice - recieved) + "");
        } else {
            advanceTextField.setText(recieved + "");
            payableTextField.setText((finalprice - recieved + ""));
            changeTextField.setText("");
        }
        
    }
